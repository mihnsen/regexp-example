/**
 * @return {boolean}
 * @description
 * Check valid Ip address V4
 * @example 192.168.1.1
 */


/*
==============
0 <= x <= 255
==============
0..9
00..99
000..199
200..249
250..255

0..199
200..249
250..255
*/
function ipAddress(str) {
  return !!str.match(/^([01]?\d{1,2}|2[0-4]\d|25[0-5])(\.([01]?\d{1,2}|2[0-4]\d|25[0-5])){3}$/g);

  return !!str.match(/^(\d|\d{2}|[01]\d{2}|2[0-4]\d|25[0-5])(\.(\d|\d{2}|[01]\d{2}|2[0-4]\d|25[0-5])){3}$/g);

  return !!str.match(/^([0-9]|[0-9]{2}|[01][0-9]{2}|2[0-4][0-9]|25[0-5])(\.([0-9]|[0-9]{2}|[01][0-9]{2}|2[0-4][0-9]|25[0-5])){3}$/g);
}
